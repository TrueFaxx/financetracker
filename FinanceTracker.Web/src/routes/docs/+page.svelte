<h1>Docs</h1>

<h2>What it does</h2>
<ul>
  <li>Import bank CSV</li>
  <li>Store transactions locally (SQLite)</li>
  <li>Dashboard: monthly totals, top merchants, biggest spends</li>
</ul>

<h2>CSV format</h2>
<p>Headers required:</p>
<ul>
  <li><code>Date</code> (ex: 2026-01-21)</li>
  <li><code>Description</code></li>
  <li><code>Amount</code> (income +, expenses -)</li>
</ul>

<h2>Privacy</h2>
<p>Local-only by default. If you expose it to the internet, that’s on you — lock it down first.</p>
